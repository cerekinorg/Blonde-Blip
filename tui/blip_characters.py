"""
Blip Characters - Digital Familiars for Blonde CLI
Multiple character options with different personalities and visual styles
"""

from typing import Dict, List, Optional
from enum import Enum


class BlipCharacter(Enum):
    """Available Blip characters"""
    AXOLOTL = "axolotl"
    WISP = "wisp"
    INKLING = "inkling"
    SPROUT = "sprout"


class CharacterArt:
    """ASCII art and colors for a Blip character"""
    
    def __init__(
        self,
        name: str,
        description: str,
        states: Dict[str, List[str]],
        color_schemes: Dict[str, str],
        personality: str
    ):
        self.name = name
        self.description = description
        self.states = states
        self.color_schemes = color_schemes
        self.personality = personality
    
    def get_art(self, state: str, frame: int = 0) -> str:
        """Get ASCII art for a state at a specific frame"""
        frames = self.states.get(state, self.states.get("idle", [""]))
        return frames[frame % len(frames)]
    
    def get_color(self, state: str) -> str:
        """Get color for a state"""
        return self.color_schemes.get(state, "white")



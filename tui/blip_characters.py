# Character registry
CHARACTERS: Dict[str, CharacterArt] = {
    "axolotl": AXOLOTL_ART,
    "wisp": WISP_ART,
    "inkling": INKLING_ART,
    "sprout": SPROUT_ART
}


def get_character(name: str) -> Optional[CharacterArt]:
    """Get a character by name"""
    return CHARACTERS.get(name.lower())


def list_characters() -> List[str]:
    """List available character names"""
    return list(CHARACTERS.keys())


def get_default_character() -> str:
    """Get the default character name"""
    return "axolotl"
